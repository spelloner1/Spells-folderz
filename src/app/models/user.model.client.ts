export class User{
	_id:String;
	username:String;
	password:String;
	firstName:String;
	lastName:String;
	email: String;

	constructor(id, username, password, firstName, lastName,email){
		this._id =_id;
		this.username = username;
		this.username = password;
		this.username = firstName;
		this.username = lastName;
		this.email = email;
	}
}